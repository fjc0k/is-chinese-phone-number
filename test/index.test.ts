import isChinesePhoneNumber from '../src'

test('test', () => {
  expect(isChinesePhoneNumber('110')).toBe(false)
  expect(isChinesePhoneNumber('120')).toBe(false)
  expect(isChinesePhoneNumber('10086')).toBe(false)
  expect(isChinesePhoneNumber('180800300800')).toBe(false)
  expect(isChinesePhoneNumber('16080030080')).toBe(true)
  expect(isChinesePhoneNumber('16080030080', true)).toBe(false)
  expect(isChinesePhoneNumber.mobile('16080030080')).toBe(true)
  expect(isChinesePhoneNumber.mobile('16080030080', true)).toBe(false)
  expect(isChinesePhoneNumber('12345678')).toBe(false)
  expect(isChinesePhoneNumber('87654321')).toBe(true)
  expect(isChinesePhoneNumber('87654321', true)).toBe(false)
  expect(isChinesePhoneNumber.landline('87654321')).toBe(true)
  expect(isChinesePhoneNumber.landline('87654321', true)).toBe(false)
  expect(isChinesePhoneNumber('10-87654321')).toBe(false)
  expect(isChinesePhoneNumber('010-87654321')).toBe(true)
  expect(isChinesePhoneNumber('010-7654321')).toBe(true)
  expect(isChinesePhoneNumber.mobile('010-7654321')).toBe(false)
  expect(isChinesePhoneNumber('01-87654321')).toBe(false)
  expect(isChinesePhoneNumber('010-987654321')).toBe(false)
  expect(isChinesePhoneNumber('010-654321')).toBe(false)
})
